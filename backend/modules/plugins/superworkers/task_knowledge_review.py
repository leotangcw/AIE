"""Task Knowledge Review Skill - 任务前知识审查

SuperWorkers 的核心工作流技能。
指导 Agent 在执行企业任务前完成知识准备工作，而不是从零出发。
"""

from typing import Any, Optional

from backend.modules.plugins.hooks import Hook


class TaskKnowledgeReviewSkill:
    """
    任务前知识审查技能

    当 Agent 接到企业任务时，指导其按以下流程工作：
    1. 分析任务 → 理解目标和涉及的领域
    2. 查本地技能 → 检查是否有匹配的技能
    3. 查企业知识 → 通过 KnowledgeHub 检索
    4. 整理参考 → 将检索结果整理成参考资料
    5. 执行任务 → 基于参考资料完成任务
    """

    @property
    def name(self) -> str:
        return "task-knowledge-review"

    def should_activate(self, message: str) -> bool:
        """不自动激活，由 using-superworkers 指向"""
        return False

    async def process(self, message: str, context: dict[str, Any]) -> Optional[str]:
        """
        返回知识审查工作流的详细指引
        """
        return self._get_workflow_guide()

    def _get_workflow_guide(self) -> str:
        """获取完整的工作流指引"""
        return """## 任务前知识审查工作流

当你接到企业任务时，建议按以下步骤进行知识准备：

### 第一步：分析任务

在开始工作之前，先花几秒钟理解任务：
- 任务的核心目标是什么？
- 涉及哪些业务领域或知识领域？
- 是常规任务还是新任务？
- 需要什么样的数据或知识？

### 第二步：检查本地技能

使用 `list_skills` 工具查看所有可用技能，看是否有匹配的：
- 如果有直接匹配的技能 → 读取并遵循技能指引
- 如果有相关但不完全匹配的技能 → 参考技能中的有用部分
- 如果没有匹配的技能 → 进入下一步

### 第三步：检索企业知识

使用 `knowledge_retrieve` 工具检索相关知识：

**检索策略**：
- 首次检索使用 `mode="auto"`（自动选择最佳模式）
- 如果结果不够，尝试 `mode="hybrid"`（混合检索，关键词+语义）
- 如果涉及实体关系（如人物、部门、项目之间的关联），尝试 `mode="graph"`
- 如果是精确的关键词匹配需求，使用 `mode="direct"`

**检索技巧**：
- 将任务拆解为多个检索维度分别查询
- 例如："Q1销售数据" 可以拆分为 "Q1销售", "销售数据分析", "销售报表格式"
- 每次检索后评估结果相关性，不相关则换关键词重试

### 第四步：补充数据查询

如果需要结构化数据，使用 `knowledge_query_db` 工具：
- 用自然语言描述你的数据需求
- 例如: "查询2026年1月的销售额按产品分类汇总"

如果企业知识不够，使用 `web_search` 补充公开信息。

### 第五步：整理参考资料

将检索到的所有信息整理成结构化参考：

```
## 参考资料

### 相关技能
- [技能名]: 技能内容摘要

### 企业知识
1. [来源]: 内容摘要（相关度: 高/中/低）
2. [来源]: 内容摘要

### 数据查询结果
- 查询: xxx
- 结果: xxx

### 参考建议
- 基于以上资料，建议的执行方案...
```

### 第六步：执行任务

基于整理好的参考资料执行任务：
- 严格参考已有知识和技能
- 如果执行过程中发现参考资料不够，可以随时回到第三步补充检索
- 知识引用时标注来源，便于追溯

### 重要原则

1. **不要跳过知识检索直接执行** - 企业任务往往有既定流程和规范
2. **检索不到也要记录** - 如果企业知识库中没有相关内容，记录下来，后续可以补充
3. **知识不是万能的** - 检索到的知识供参考，如果明显不适用可以不用
4. **来源追溯** - 重要结论标注知识来源，方便后续验证

---

你可以现在就开始按这个流程处理当前任务。如果需要查看企业的知识源列表，使用 `knowledge_list_sources` 工具。"""

    def get_hooks(self) -> list[Hook]:
        """不注册自动 Hook"""
        return []
